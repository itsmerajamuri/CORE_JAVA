package com.java.operators;

public class Operators {

	public static void main(String[] args) {
		// +
		int a,b;
		a=10;
		b=20;
		a=10;
		b=20;
		System.out.println(a+b);
		// -
		int c,d;
		c=20;
		d=1;
		System.out.println(c-d);
		// *
		int e,f;
		e=20;
		f=5;
		System.out.println(e*f);
		// /
		int g,h;
		g=20;
		h=5;
		System.out.println(g/h);
		// %
		int i,j;
		i=20;
		j=2;
		System.out.println(i%j);

	}

}
