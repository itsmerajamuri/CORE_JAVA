package com.java.operators;

public class IncrementOperators {

	public static void main(String[] args) {
		// ++(pre-increment)
		int a=10;
		int b=++a;
		System.out.println("a value is " +b +" && "+"b value is "+a);
		// ++(post-increment)
		int c=20;
		int d=c++;
		System.out.println("c value is " +c +" && "+"d value is "+d);
		// ++(pre-decrement)
				int e=10;
				int f=--e;
				System.out.println("e value is " +e +" && "+"f value is "+f);
				// ++(post-decrement)
				int g=20;
				int h=g--;
				System.out.println("g value is " +g +" && "+"h value is "+h);

				

		
		
		

	}

}
