package com.java.conditionalstatements;

public class SwitchStatement {

	public static void main(String[] args) {
		int a=10;
		int b=28;
		switch(a+b) {
		case 28:
			System.out.println("a equal to 28");
			break;
		case 38:
			System.out.println("a equal to 38");
			break;
		case 10:
			System.out.println("a equal to 10");
			break;
		default:
			System.out.println("false");
			break;

		
		
	}

}
}