package com.java.looping;

public class InnerForLoop {

	public static void main(String[] args) {
		// inner for loop
		int a=10;
		int b=26;
		// outer for loop (rows)
		for(int i=0;i<3;i++) {
			// inner for loop (columns)
			for(int j=0;j<3;j++) {
				System.out.println("I'am Bablu");
			}
		}
		

	}

}
